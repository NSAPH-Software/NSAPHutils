#' @title
#' Sum Three Numerical Values
#'
#' @description
#' This functions sums three numerical values.
#'
#' @param a first numerical parameter
#' @param b second numerical parameter
#' @param c third numerical parameter
#'
#' @author Naeem Khoshnevis, \email{nkhoshnevis@g.harvard.edu}
#'
#' @return
#' The sum of \code{a}, \code{b}, and \code{c}
#'
